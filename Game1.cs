        /// <summary>
        /// Allows the game to run logic such as updating the world,
        /// checking for collisions, gathering input, and playing audio.
        /// </summary>
        /// <param name="gameTime">Provides a snapshot of timing values.</param>
        protected override void Update(GameTime gameTime)
        {
            // Allows the game to exit
            if (GamePad.GetState(PlayerIndex.One).Buttons.Back == ButtonState.Pressed)
                this.Exit();
            MouseState mouse = Mouse.GetState();
            Rectangle mouseRectangle = new Rectangle(mouse.X, mouse.Y, 1, 1);
            healthrect = new Rectangle(50, 1350, player.health, 20);


            if (mouseRectangle.Intersects(player.rect) && (mouse.LeftButton == ButtonState.Pressed && prevMouse.LeftButton==ButtonState.Released))
            {
                player.health -= 10;
            }



            player.Update(gameTime);
            prevMouse = mouse;
            foreach(TileCollision tile in map.TileCollision)
            {
                player.Collision(tile.Rectangle, map.Width, map.Height);
                camera.Update(player.Position, map.Width, map.Height);
            }

            if(Keyboard.GetState().IsKeyDown(Keys.F) && prevKey.IsKeyUp(Keys.F))
            {
                Shoot();
            }

            prevKey = Keyboard.GetState();
            UpdateBullets();

            base.Update(gameTime);
        }

        public void UpdateBullets()
        {
            foreach(Bullets bullet in bullets)
            {
                bullet.position += bullet.velocity;
                if (Vector2.Distance(bullet.position, player.position) >500)
                {
                    bullet.isVisible = false;
                }
            }

            for (int i = 0; i< bullets.Count; i++)
            {
                if (!bullets[i].isVisible)
                {
                    bullets.RemoveAt(i);
                }
            }
        }

        public void Shoot()
        {
            Bullets newBullet = new Bullets(Content.Load<Texture2D>("bullet1"));
            newBullet.velocity = new Vector2(0,-15);
            newBullet.position = player.position + newBullet.velocity * 5;
            newBullet.isVisible = true;
            
            if(bullets.Count() <20)
            {
                bullets.Add(newBullet);
            }
        }

